var listar_Personas_departamento = function (id) {

    $('#tablapersonas_departamento tbody').off('click', 'tr');
    $('#tablapersonas_departamento tbody').off('dblclick', 'tr');

    var table = $("#tablapersonas_departamento").DataTable({
        "destroy": true,
        "ajax": {
            url: server + "index.php/personas_control/Cargar_personas_departamento",
            dataType: "json",
            data: {
                id: id
            },
            "dataSrc": function (json) {
                if (json.length == 0) {
                    return Array();
                }
                return json.data;
            },
            type: "post",
        }, //paging: false,
        //scrollY: 400,
        "processing": true,
        "columns": [
            { "data": "nombre" },
            { "data": "segundo_nombre" },
            { "data": "apellido" },
            { "data": "segundo_apellido" },
            { "data": "identificacion" },
            { "data": "telefono" },
            { "data": "correo" },
        ], "language": idioma,
        dom: 'Bfrtip',
        "buttons": [
            {
                extend: 'excelHtml5',
                text: '<i class="fa fa-file-excel-o"></i>',
                titleAttr: 'Excel',
                className: 'btn btn-success',
            },
            {
                extend: 'csvHtml5',
                text: '<i class="fa fa-file-text-o"></i>',
                titleAttr: 'CSV',
                className: 'btn btn-default',
            },
            {
                extend: 'pdfHtml5',
                text: '<i class="fa fa-file-pdf-o"></i>',
                titleAttr: 'PDF',
                className: 'btn btn-danger2',
            }
        ]

    });

}

